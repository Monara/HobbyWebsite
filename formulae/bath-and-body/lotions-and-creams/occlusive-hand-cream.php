				In an attempt to counter that, I have added phenonip as an effective preservative (phenoxyethanol + parabens), lowered the water activity with 10% glycerin, and kept the pH low (30% solution at pH 5.4). 
				I came up with the exorbitant amount of glycerin after reviewing some sources that claim that similar high humectant/high occlusive/low pH combinations are good for such problematic skin. Contrary to the popular myth, glycerin doesn't have to sticky in large amounts, at least not in a thick cream formula. It has actually provided a slightly yoghurty feel.
				
