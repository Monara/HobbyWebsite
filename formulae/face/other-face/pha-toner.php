				<h5>Notes</h5>
				<p>I started by heating GDL and water to 70&#176;C and holding for a few minutes. I then added glycerin, sodium PCA, glycerite, glycine, and PEG-40 HCO and stirred until HCO was dissolved.
				 I then let it cool to below 40&#176;C and adjusted the pH from 2.9 to 3.6 (so it took over 0.1% of the hydroxide solution to shift by 0.1 point). Lastly I added a water-soluble preservative, fragrance (cardamom aldehyde in my case - a very airy scent), and allantoin, all of which generally should not be heated. 
				 I also added just a hint of some water-based dye, which did not even make up even a full drop in a 100 gram batch: adding more, subtract from water. 
				<br><br>As usual, the pH should always be tested and preservative quantities adjusted according to supplier recommendations. If you plan on heating and holding for longer, don't forget to weigh your beaker before and after to account for water loss. Note that if you're using more or less of GDL or sodium PCA it will very much affect the final amount of hydroxide needed. If you use chelators in addition to preservatives, they are not necessary in this formula as GDL acts as one.</p>
