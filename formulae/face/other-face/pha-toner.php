				<p>To reiterate, a reliable pH meter in non-negotiable in such a low pH formulation - make sure it is calibrated. Read about and acquire appropriate protective gear. Protective goggles, gloves, long sleeves are all essential when working with acids and sodium hydroxide. Don't burn or permanently blind yourself.
				<br><br>Before I started working on this formula, I was researching similar products as usual. I came across a 3% PHA serum from Inkey List. What I found curious is that while they claim a pH of 3.62, the product also contains 3% niacinamide.
				 I have been formulating with the wide-spread idea that niacinamide is most stable at near-neutral pH like 6.0, and it slowly turns into niacin that can cause redness and flushing if exposed to low pH or heat. It looks like something that warrants some further digging for another occasion.				
				<br><br>This formula has a few minor challenges. While glucono-delta-lactone has good solubility in water, it is unusual in being a gradual acidifier rather than an instant one like the many acids you may be used to. 
				Added to water, it slowly hydrolyzes into gluconic acid. It is key to ensure that the conversion is finished as much as possible before bottling, as the pH might fall below the safe levels. 
				As you can see from the image below, the process of GDL hydrolyzing into gluconic acid is gradual at room temperature: the pH stabilizes over the course of 2 hours. 
				Heat makes the process faster. I cannot find the reference again, but it said that for every extra 10 degrees Celsius, the hydrolysis process is shortened in half. 
				That's why I start by heating water and GDL: at 65&#176;C it should take 7.5 minutes. I did retest the pH after a week and it had remained stable.
				<br><br><img src="/img/postimg/gdl_hydrolysis.jpg" onerror="this.onerror=null; this.src='/img/default.jpg'" style="width:80%" alt="Gluconodeltalactone hydrolysis">
				<br><br>This formula also incorporates a bit of fragrance. As it is a water-based formula, a bit of solubilizer is necessary.  
				I have used PEG-40 hydrogenated castor oil, but something like a polysorbate would work too (amount may differ).
				<br><br>Since it is a low pH formula with fewer preservation worries, I opted to include an extract - a cucumber glycerite I made a while ago. 
				Making it simply involves covering raw cucumber (less than 40%) with glycerin (over 60% for low water activity). Being a humectant, glycerin essentially absorbs the water from the cucumbers.
				 After a week or two the cucumbers can be taken out and weighed so that you know the extract concentration. Mine was over 80% glycerin in the end (having absorbed around 16% of starting cucumber weight). 
				 While a glycerite on it's own smells quite nice (especially a strawberry one), it does not impart any scent to the final product at reasonable concentrations.
				  It doesn't do anything really either. Extracts make for nice marketing stories and are mostly used at minuscule amounts in the industry just to appear on the ingredients list - they present a challenge to the preservation system without any real functional pay off. So don't go crazy with extracts as beginners often do!
				   But I was curious about the process, as I often see such extracts sold at not-so-low prices. A glycerite can be easily replaced with plain glycerin, and no one would ever know.</p>
